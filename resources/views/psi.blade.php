</div>

<div class="max-w-4xl mx-auto py-8 px-4">
  
  <!-- 1º tópico: Objetivo e Escopo -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>1. Objetivo e Escopo</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">1.1. Esta Política de Segurança da Informação (PSI) estabelece diretrizes, responsabilidades e procedimentos para garantir a segurança e a confidencialidade dos dados sensíveis dos pacientes no Sistema de Referenciamento Fisioterapêutico (SRF).</p>
      <p class="mb-4">1.2. Esta política se aplica a todos os colaboradores, contratados e prestadores de serviços que tenham acesso ao SRF e aos dados dos pacientes.</p>
    </div>
  </div>
  
  <!-- 2º tópico: Classificação da Informação -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>2. Classificação da Informação</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">2.1. Os dados dos pacientes serão classificados de acordo com o seu nível de sensibilidade, sendo categorizados como:
         <br>a) Dados Pessoais: Informações de identificação pessoal dos pacientes.
         <br>b) Dados de Saúde: Informações médicas e de saúde dos pacientes, incluindo histórico médico, diagnósticos e tratamentos.</p>
      <p class="mb-4">2.2. A classificação da informação será utilizada para determinar os controles de segurança apropriados para cada tipo de dado.</p>
    </div>
  </div>
  
  <!-- 3º tópico: Acesso e Controle de Dados -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>3. Acesso e Controle de Dados</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">3.1. O acesso ao SRF e aos dados dos pacientes será concedido apenas a usuários autorizados, de acordo com a necessidade de realizar suas funções.
      <br>3.2. Cada usuário terá uma conta de usuário exclusiva, protegida por credenciais de acesso individuais.
      <br>3.3. O acesso aos dados sensíveis dos pacientes será restrito com base no princípio do menor privilégio, ou seja, os usuários terão acesso apenas às informações necessárias para realizar suas atividades.
      <br>3.4. Serão implementados controles de autenticação e autorização robustos para garantir a segurança do acesso ao SRF.</p>
    </div>
  </div>
  
  <!-- 4º tópico: Proteção de Dados -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>4. Proteção de Dados</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">4.1. Todos os dados armazenados no SRF serão protegidos contra acesso não autorizado, modificação, divulgação ou destruição.
      <br>4.2. Serão implementadas medidas técnicas e organizacionais adequadas para garantir a integridade, confidencialidade e disponibilidade dos dados.
      <br>4.3. Serão realizados backups regulares dos dados do SRF para garantir a sua recuperação em caso de incidentes ou falhas.</p>
    </div>
  </div>
  
  <!-- 5º tópico: Monitoramento e Auditoria -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>5. Monitoramento e Auditoria</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">5.1. Serão implementados mecanismos de monitoramento contínuo para detectar e responder a atividades suspeitas no SRF.
      <br>5.2. Serão realizadas auditorias regulares para avaliar a conformidade com esta PSI e identificar possíveis áreas de melhoria na segurança da informação.</p>
    </div>
  </div>
  
  <!-- 6º tópico: Treinamento e Conscientização -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>6. Treinamento e Conscientização</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">6.1. Todos os usuários terão acesso a treinamentos periódicos sobre segurança da informação, incluindo boas práticas de uso do SRF, prevenção de ameaças cibernéticas e procedimentos em caso de incidentes de segurança.
      <br>6.2. Será promovida uma cultura de segurança da informação, incentivando os usuários a relatarem quaisquer suspeitas de violação de segurança ou práticas inadequadas.</p>
    </div>
  </div>
  
  <!-- 7º tópico: Responsabilidades -->
  <div class="border-b border-gray-300 mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>7. Responsabilidades</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">7.1. A equipe de TI será responsável por implementar e manter os controles de segurança técnica no SRF.
      <br>7.2. A equipe de gestão será responsável por supervisionar a implementação desta PSI, garantir a conformidade com as políticas e procedimentos de segurança e promover uma cultura de segurança da informação entre os usuários.
      <br>7.3. Todos os usuários serão responsáveis por cumprir esta PSI, proteger os dados dos pacientes e relatar quaisquer incidentes de segurança ou violações de dados.</p>
    </div>
  </div>
  
  <!-- 8º tópico: Revisão e Atualização -->
  <div class="mb-6">
    <h2 class="text-lg font-bold mb-2"><strong>8. Revisão e Atualização</strong></h2>
    <div class="grid grid-cols-1 gap-4">
      <p class="mb-4">8.1. Esta PSI será revisada e atualizada regularmente para garantir sua eficácia contínua e conformidade com os requisitos legais e regulamentares aplicáveis.</p>
    </div>
  </div>
  
